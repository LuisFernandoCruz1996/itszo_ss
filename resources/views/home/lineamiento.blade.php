      <div class="modal-content">
        <div class="modal-header">
          Definición y Caracterización
          <button type="button" class="close" data-dismiss="modal" aria-label="Close"><span aria-hidden="true">&times;</span></button>            
        </div>
          <div class="modal-body">
          <p>
            Se entiende por Servicio Social el trabajo de carácter temporal y obligatorio, que institucionalmente presten y ejecuten los estudiantes en beneficio de la sociedad. Los estudiantes de las Instituciones adscritas al TecNM prestan el Servicio Social para cumplir con los créditos de sus planes de estudios. 
          </p>
          <p>
            La finalidad del Servicio Social es fortalecer la formación integral del estudiante, desarrollando una conciencia de solidaridad y compromiso con la sociedad a la que pertenece, mediante la aplicación y desarrollo de sus competencias profesionales. Para efectos del presente lineamiento se entiende como prestador al estudiante que realiza la prestación del Servicio Social en alguna dependencia, bajo la vigilancia de un supervisor inmediato. 
          </p>
          <p>
            La prestación del Servicio Social puede realizarse en dependencias públicas y organismos privados que cuenten con programas de asistencia social y desarrollo comunitario establecidos en el Plan Nacional de Desarrollo vigente y contribuyan al desarrollo económico, social y cultural de la Nación. 
          </p>
          <p>
            Los programas de Servicio Social comunitario pueden ser de educación para adultos; programas de contingencia; cuidado al medio ambiente y desarrollo sustentable; apoyo a la salud; promotores y/o instructores de actividades culturales, deportivas y cívicas; operación de programas especiales gubernamentales; programas productivos en beneficio social que permitan el desarrollo profesional del estudiante; y programas establecidos específicamente por el Instituto que estén relacionados con las acciones antes mencionadas.
          </p>
          <p> 
            Los programas de Servicio Social profesionalizante pueden ser proyectos integradores, proyectos de investigación, proyectos de Innovación Tecnológica, proyectos de emprendedurismo, operación de programas especiales gubernamentales relacionados con su carrera, programas empresariales de responsabilidad social, programas productivos que permitan el desarrollo profesional del estudiante, y programas establecidos específicamente por el Instituto que estén relacionados con las acciones antes mencionadas.
          </p>
          </div>
      </div>
