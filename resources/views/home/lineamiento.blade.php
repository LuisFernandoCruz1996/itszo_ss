  <div class="modal video-modal fade" id="politicasgeneralidades" tabindex="-1" role="dialog" aria-labelledby="politicasgeneralidades">
    <div class="modal-dialog" role="document">
      <div class="modal-content">
        <div class="modal-header">
          Políticas de Operación: Generalidades
          <button type="button" class="close" data-dismiss="modal" aria-label="Close"><span aria-hidden="true">&times;</span></button>            
        </div>
          <div class="modal-body">
            <p>
            El Departamento de Gestión Tecnológica y Vinculación o su equivalente en los Institutos Tecnológicos Descentralizados, presenta las propuestas de proyectos de Servicio Social ante el Comité Académico para su revisión y recomendación al (a la) Director(a) del Instituto, quien emite su autorización.
          </p>
          <p>
            El Departamento de Gestión Tecnológica y Vinculación o su equivalente en los Institutos Tecnológicos Descentralizados es la instancia que registra, recluta y autoriza la prestación del Servicio Social al estudiante.
          </p>
          <p> 
            Los estudiantes pueden prestar su Servicio Social una vez aprobado el porcentaje de créditos de acuerdo con la normativa vigente, indicada en el apartado de alcance de este documento.
            El Servicio Social tiene un valor de 10 créditos en los planes de estudio, donde un crédito equivale a 50 horas.
          </p>
          <p>
            Para ser liberado el Servicio Social se tiene que cumplir con los 10 créditos en un periodo no menor de seis meses.
          </p>
          <p>
            Los prestadores que trabajen en dependencias federales, estatales y municipales y opten por acreditar el Servicio Social, deben cumplir con los siguientes requisitos:
          </p>
          <p>
            Presentar documentación que lo acredite como trabajador con una antigüedad mínima de seis meses.
          </p>
          <p>
            Presentar documentos que justifiquen que las labores realizadas en el organismo y área de adscripción, corresponden y cumplen con el objetivo que se pretende alcanzar al prestar el Servicio Social, según lo establecido en el presente lineamiento en su propósito, definición y caracterización del Servicio Social.
          </p>
          <p>
            Respetar y cumplir las obligaciones establecidas en el apartado del prestador del Servicio Social.
          </p>
          <p>
            El informe final deberá corresponder con las actividades realizadas a partir de la solicitud de prestación del Servicio Social y autorización respectiva.
          </p>
          <p>
            Para la realización del Servicio Social en un ámbito de influencia diferente al del Instituto, los prestadores deben cubrir los siguientes requisitos:
          </p>
          <p>
            Presentar la solicitud de autorización para realizar el Servicio Social en otras instituciones de Educación Superior.
          </p>
          <p>
            Entregar a la dependencia seleccionada, oficio de autorización expedido por el Instituto de procedencia, indicándole que no hay inconveniente en que se le coordine su actividad de Servicio Social.
          </p>
          <p>
            Verificar cuando concluya su programa de Servicio Social, que la institución seleccionada envíe los instrumentos de evaluación cualitativa al desempeño (Anexo XXIII), y la carta de terminación (Anexo XIX) al Instituto de procedencia.
          </p>
          <p>
            Los programas de Servicio Social propios del Instituto relacionados con actividades culturales, deportivas y cívicas, solamente se autorizan con carácter de promotores y/o instructores, y cuando sea en beneficio de la sociedad y autorizado por el (la) Director(a) del Instituto, previa recomendación del Comité Académico.
          </p>
          </div>
