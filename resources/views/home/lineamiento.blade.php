    <div class="modal-dialog" role="document">
      <div class="modal-content">
        <div class="modal-header">
          Políticas de Operación: Generalidades
          <button type="button" class="close" data-dismiss="modal" aria-label="Close"><span aria-hidden="true">&times;</span></button>            
        </div>
          <div class="modal-body">
            <p>
            El responsable de la dependencia donde se realiza el servicio social evalúa al prestador de manera bimestral y final, mediante el Formato de Evaluación Cualitativa del Prestador de Servicio Social (Anexo XXIII) y la carta de terminación de Servicio Social (Anexo XIX), que el prestador entrega al Departamento de Gestión Tecnológica y Vinculación o su equivalente en los Institutos Tecnológicos Descentralizados.
          </p>
          <p> 
            El Departamento de Gestión Tecnológica y Vinculación o su equivalente en los Institutos Tecnológicos Descentralizados, con base a la valoración cualitativa del desempeño, el cumplimiento de los informes bimestrales y final; determina si alcanza el desempeño excelente, notable, bueno, suficiente o insuficiente del Servicio Social. 
          </p>
          <p>
            En el caso de desempeño excelente, notable, bueno y suficiente, se emite la constancia de término de Servicio Social (Anexo XXVI) y la turna al Departamento de Servicios Escolares o su equivalente en los Institutos Tecnológicos Descentralizados. 
          </p>
          <p>
            Con base en el punto anterior, en el Certificado de estudios del estudiante se asienta el nivel de desempeño que se encuentra en la constancia de término de Servicio Social (Anexo XXVI).
          </p>
